# Converte float para inteiro (trunca decimal)
print(int(1.98346786))  # Saída: 1

# Converte string para inteiro
print(int('10'))  # Saída: 10

# Converte string para float
print(float('10.10'))  # Saída: 10.1

# Converte inteiro para float
print(float(100))  # Saída: 100.0

# Armazena o valor 10 em 'valor'
valor = 10

# Converte o inteiro para string
valor_str = str(valor)

# Exibe o tipo de 'valor' (inteiro)
print(type(valor))  # Saída: <class 'int'>

# Exibe o tipo de 'valor_str' (string)
print(type(valor_str))  # Saída: <class 'str'>

# Divisão comum (resultado float)
print(100 / 2)  # Saída: 50.0

# Divisão inteira (descarta resto)
print(100 // 2)  # Saída: 50
